    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        if (txtLD.getText().equals("") || txtPB.getText().equals("") |
            txtUsia.getText().equals("") | cmbJK.getSelectedIndex() == 0) {
            pesan.setText("LENGKAPI DATA!!!");
            txtJudulHasil.setText("");
            txtBB.setText("");
            txtHarga.setText("");
            pesan.setForeground(Color.YELLOW);
        } else {
            pesan.setText("");
            txtJudulHasil.setText("HASIL");
            setLd(Double.parseDouble(txtLD.getText()));
            setPb(Double.parseDouble(txtPB.getText()));
            double hasil = (2 * getLd() * getPb()) / 104;
            txtBB.setText("Berat Badan = " + String.valueOf(hasil));
            int harga;
            if (cmbJK.getSelectedIndex() == 1) {
                harga = (int) (hasil * 75000) + 200000;
                txtHarga.setText("Harga = Rp. "+harga);
            } else {
                harga = (int) (hasil * 75000);
                txtHarga.setText("Harga = Rp. "+harga);
            }

        }
    }//GEN-LAST:event_btnHitungActionPerformed

